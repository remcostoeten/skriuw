import { useEffect, useRef, useState } from 'react'

const DIGITS_PER_SET = 10

const DIGITS = Array.from({ length: 30 }, (_, i) => i % DIGITS_PER_SET)

type TSlotDigit = {
	digit: number
	trigger: boolean
	duration: number
	className?: string
	scrollDistance: number
	targetSetIndex: number
	gradientHeight: number
	initialBlur: number
	easing: string
}

function SlotDigit({
	digit,
	trigger,
	duration,
	className,
	scrollDistance,
	targetSetIndex,
	gradientHeight,
	initialBlur,
	easing
}: TSlotDigit) {
	const targetOffset = digit + targetSetIndex * DIGITS_PER_SET
	const initialOffset = targetOffset - scrollDistance
	const itemHeightPercent = 100 / 30

	const transformInitial = `translateY(-${initialOffset * itemHeightPercent}%)`
	const transformTarget = `translateY(-${targetOffset * itemHeightPercent}%)`

	return (
		<span
			className={`relative inline-block overflow-hidden h-[1em] leading-none align-baseline ${className || ''}`}
		>
			<span className="opacity-0">{digit}</span>

			<span
				className="absolute top-0 left-0 right-0 flex flex-col items-center will-change-transform"
				style={{
					transform: trigger ? transformTarget : transformInitial,
					filter: trigger ? 'blur(0px)' : `blur(${initialBlur}px)`,
					transition: `transform ${duration}ms ${easing} 0s, filter ${duration}ms ease-out 0s`
				}}
				aria-hidden="true"
			>
				{DIGITS.map((num, i) => (
					<span
						key={i}
						className="flex items-center justify-center h-[1em] leading-none select-none"
					>
						{num}
					</span>
				))}
			</span>

			<span
				className="absolute inset-x-0 top-0 bg-gradient-to-b from-[#121212] to-transparent z-10 pointer-events-none"
				style={{ height: `${gradientHeight}%` }}
			/>
			<span
				className="absolute inset-x-0 bottom-0 bg-gradient-to-t from-[#121212] to-transparent z-10 pointer-events-none"
				style={{ height: `${gradientHeight}%` }}
			/>
		</span>
	)
}

type TProps = {
	value: string | number
	className?: string
	duration?: number
	staggerDelay?: number
	scrollDistance?: number
	targetSetIndex?: number
	gradientHeight?: number
	initialBlur?: number
	easing?: string
}

export function AnimatedNumber({
	value,
	className,
	duration = 800,
	staggerDelay = 100,
	scrollDistance = 12,
	targetSetIndex = 2,
	gradientHeight = 20,
	initialBlur = 1,
	easing = 'cubic-bezier(0.2, 0.8, 0.4, 1)'
}: TProps) {
	const [isVisible, setIsVisible] = useState(false)
	const elementRef = useRef<HTMLSpanElement>(null)
	const stringValue = String(value)

	useEffect(() => {
		const frameId = requestAnimationFrame(() => {
			setIsVisible(true)
		})

		return () => cancelAnimationFrame(frameId)
	}, [])

	const chars = stringValue.split('')
	let digitIndex = 0

	return (
		<span
			ref={elementRef}
			className={`inline-flex items-baseline whitespace-pre-wrap ${className || ''}`}
			aria-label={stringValue}
		>
			<span className="sr-only">{stringValue}</span>

			<span className="inline-flex items-baseline" aria-hidden="true">
				{chars.map((char, i) => {
					if (/\d/.test(char)) {
						const currentDigitDuration =
							duration + digitIndex * staggerDelay
						const currentDigitIndex = digitIndex
						digitIndex++

						return (
							<SlotDigit
								key={i}
								digit={parseInt(char, 10)}
								trigger={isVisible}
								duration={currentDigitDuration}
								scrollDistance={scrollDistance}
								targetSetIndex={targetSetIndex}
								gradientHeight={gradientHeight}
								initialBlur={initialBlur}
								easing={easing}
							/>
						)
					}
					return <span key={i}>{char}</span>
				})}
			</span>
		</span>
	)
}
