def animate():
    frames = get_frames()

    for frame in frames:
        print_frame(frame)
        time.sleep(0.35)

    time.sleep(1.2)

    for _ in range(5):
        print_frame(frames[-1])
        time.sleep(0.25)
        print_frame(frames[-2])
        time.sleep(0.25)

    for frame in reversed(frames[:-1]):
        print_frame(frame)
        time.sleep(0.18)

    clear_screen()

if __name__ == "__main__":
    animate()
